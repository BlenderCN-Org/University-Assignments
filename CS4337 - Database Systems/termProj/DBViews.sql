);

CREATE VIEW View4 (Part_Purchase_Cost, Product) AS (
  SELECT SUM(X.PRICE * X.QUANTITY), X.PRODUCTTYPE 
   FROM (SELECT DISTINCT PART.PRICE, PP.Quantity, PRODUCT.PRODUCTTYPE 
           FROM PRODUCT PRODUCT, PART PART, PRODUCT_PARTS PP 
          WHERE (PP.PRODUCTID = PRODUCT.PRODUCTID AND PART.PARTID = PP.PARTID)) X 
   GROUP BY X.PRODUCTTYPE
);