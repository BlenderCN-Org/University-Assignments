  SELECT COUNT(*), X.POTENTIALEMPLOYEEID FROM 
    (SELECT DISTINCT I.INTERVIEWID, PE.POTENTIALEMPLOYEEID 
       FROM PotentialEmployee PE, Interview I 
      WHERE (I.APPLICANTID = PE.POTENTIALEMPLOYEEID)) X, Interview_Grade IG 
   WHERE IG.INTERVIEWID = X.INTERVIEWID AND IG.GRADE > 60 GROUP BY X.POTENTIALEMPLOYEEID
);

